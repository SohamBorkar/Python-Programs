# Creating Custom Exceptions
# In Python, users can define custom exceptions by creating a new class. 
# This exception class has to be derived, either directly or indirectly, from 
# the built-in Exception class. Most of the built-in exceptions are also derived
#  from this class.

# When we are developing a large Python program, it is a good practice
#  to place all the user-defined exceptions that our program raises in a
#   separate file. Many standard modules do this. They define their exceptions 
#   separately as exceptions.py or errors.py (generally but not always).

